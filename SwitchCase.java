public class SwitchCase {
    public static void main(String[] args) {
        int score = 4;

        switch (score){
            case 1:
                System.out.println(1);
                break;
            case 4:
                System.out.println(4);
                break;
            default:
                System.out.println("dsfsdfs");
        }
    }
}
